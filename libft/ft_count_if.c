int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int count;

	i = 0;
	count = 0;
	if (!tab || !f)
		return (-1);
	while (tab[i] != 0)
	{
		if (f(tab[i]))
		{
			count++;
		}
		i++;
	}
	return (count);
}
