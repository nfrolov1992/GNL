#include <unistd.h>

void	ft_putnbr(int n)
{
	char val;
	long i;

	i = n;
	if (i < 0)
	{
		write(1, "-", 1);
		i = i * (-1);
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
	{
		val = i + 48;
		write(1, &val, 1);
	}
}
