#include <unistd.h>

void	ft_putstr_fd(char const *s, int fd)
{
	int i;

	i = 0;
	if (s != NULL && fd >= 0)
	{
		while (s[i] != '\0')
			i++;
		write(fd, s, i);
	}
}
