int		ft_sqrt(int nb)
{
	int i;
	int c;

	i = 1;
	if (nb > 0 && nb <= 2147395600)
	{
		while (i * i <= nb)
		{
			c = i * i;
			if (c == nb)
			{
				return (i);
			}
			i++;
		}
	}
	else
	{
		return (0);
	}
	return (0);
}
