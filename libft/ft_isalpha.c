int		ft_isalpha(int c)
{
	if (c >= 0x41 && c <= 0x5A)
	{
		return (1);
	}
	if (c >= 0x61 && c <= 0x7A)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
